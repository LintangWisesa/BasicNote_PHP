<?php
$warna = "merah";

switch ($warna) {
    case "biru":
        echo "Anda suka warna biru!";
        break;
    case "merah":
        echo "Anda suka warna merah!";
        break;
    case "hijau":
        echo "Anda suka warna hijau!";
        break;
    default:
        echo "Anda tidak suka biru, merah atau hijau.";
}
?>
<?php  

//The PHP while Loop

echo "<br>";
$a = 1;
 
while($a <= 10) {
  echo "The number is: $a <br>";
  $a++;
} 

//The PHP do...while Loop

echo "<br>";
$b = 1; 

do {
    echo "The number is: $b <br>";
    $b++;
} while ($b <= 10);

//The PHP for Loop

echo "<br>";
for ($c = 1; $c <= 10; $c++) {
  echo "The number is: $c <br>";
}

?>